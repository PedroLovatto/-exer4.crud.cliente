    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        clc.create(jTextFieldNome.getText(), jTextFieldCPF.getText(), jTextFieldTelefone.getText(),
                jTextFieldDtAniversario.getText(), jTextFieldEmail.getText());
        
        this.limpaCampos();
        this.getListaClientes();
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        if (jTableClientes.getSelectedRow() != -1) {
            clc.update((int) jTableClientes.getValueAt(jTableClientes.getSelectedRow(), 0),
                    jTextFieldNome.getText(), jTextFieldCPF.getText(), jTextFieldTelefone.getText(),
                    jTextFieldDtAniversario.getText(), jTextFieldEmail.getText());

        }
        this.getListaClientes();
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

