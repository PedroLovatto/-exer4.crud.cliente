    public void getListaClientes(){
     DefaultTableModel modeloTabelaClientes = (DefaultTableModel) jTableClientes.getModel();
     modeloTabelaClientes.setNumRows(0);
     
     for (Cliente c : clc.read()){
         
         modeloTabelaClientes.addRow(new Object[]{
             
             c.getIdCliente(),
             c.getNome(),
             c.getCpf(),
             c.getTelefone(),
             c.getDataAniversario(),
             c.getEmail()});
     }
    }
    
    public void limpaCampos(){
        jTextFieldNome.setText("");
        jTextFieldCPF.setText("");
        jTextFieldTelefone.setText("");
        jTextFieldDtAniversario.setText("");
        jTextFieldEmail.setText("");
        jTextFieldNome.requestFocus();
    }
    

